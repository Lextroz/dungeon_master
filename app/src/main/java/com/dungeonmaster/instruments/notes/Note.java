package com.dungeonmaster.instruments.notes;

public class Note {

    private Long id;
    private String name;
    private String notes;

    public Note(){
        name = "New Note";
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNotes() {
        return notes;
    }

    public void setNotes(String notes) {
        this.notes = notes;
    }
}
